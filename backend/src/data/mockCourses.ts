// Helper function to get course by code
export function getCourseByCode(courseCode: string): ScheduleCourse | undefined {
  return mockCourses.find(course => course.courseCode === courseCode);
}

// Helper function to search courses
export function searchCourses(query: string): ScheduleCourse[] {
  const lowerQuery = query.toLowerCase();
  return mockCourses.filter(
    course =>
      course.courseCode.toLowerCase().includes(lowerQuery) ||
      course.courseName.toLowerCase().includes(lowerQuery) ||
      course.department.toLowerCase().includes(lowerQuery)
  );
}

// Helper function to get courses by department
export function getCoursesByDepartment(department: string): ScheduleCourse[] {
  return mockCourses.filter(course => course.department === department);
}

// Helper function to get all departments
export function getAllDepartments(): string[] {
  return Array.from(new Set(mockCourses.map(course => course.department)));
}

