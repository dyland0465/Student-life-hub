import type { ScheduleCourse, CourseSection, ClassSchedule } from '../types/index';

// Helper to create time strings
const time = (hour: number, minute: number = 0): string => {
  return `${hour.toString().padStart(2, '0')}:${minute.toString().padStart(2, '0')}`;
};

// Helper to create class schedules
const createSchedule = (
  days: ClassSchedule['day'][],
  startTime: string,
  endTime: string
): ClassSchedule[] => {
  return days.map(day => ({ day, startTime, endTime }));
};

